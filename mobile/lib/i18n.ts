export type Language = 'en' | 'fr' | 'ar';
export const languageLabels: Record<Language, string> = {
  en: translations.en.english,
  fr: translations.fr.french,
  ar: translations.ar.arabic,
};

export const DEMO_CREDENTIALS = {};

export const MONTH_LABELS: Record<Language, string> = {
  en: 'April 2026',
  fr: 'Avril 2026',
  ar: 'أبريل 2026',
};

export const WEEKDAYS: Record<Language, string[]> = {
  en: ['M', 'T', 'W', 'T', 'F', 'S', 'S'],
  fr: ['L', 'M', 'M', 'J', 'V', 'S', 'D'],
  ar: ['ن', 'ث', 'ر', 'خ', 'ج', 'س', 'ح'],
};

export const VISIT_TYPES = ['consultation', 'followUp', 'routineCheckup', 'emergency'] as const;

type TranslationKey = keyof typeof translations.en;

export function useTranslation() {
  const language = useLanguageStore((state) => state.language);
  const setLanguage = useLanguageStore((state) => state.setLanguage);

  const t = (key: TranslationKey) => translations[language][key];

  const getWeekdays = () => WEEKDAYS[language];
  const getMonthLabel = () => MONTH_LABELS[language];
  const getVisitTypeLabel = (key: (typeof VISIT_TYPES)[number]) => translations[language][key];
  const getGenderLabel = (key: 'male' | 'female' | 'other' | 'preferNotToSay') =>
    translations[language][key];

  return {
    language,
    setLanguage,
    isRTL: language === 'ar',
    t,
    getWeekdays,
    getMonthLabel,
    getVisitTypeLabel,
    getGenderLabel,
    languageLabels,
    demoCredentials: DEMO_CREDENTIALS,
  };
}

export function useLanguageTextDirection() {
  const language = useLanguageStore((state) => state.language);
  return useMemo(() => language === 'ar', [language]);
}
export function convertToArabicName(name: string): string {
  if (!name) return name;
  const lower = name.toLowerCase();
  if (lower.includes('alex')) return 'أليكس جونسون';
  if (lower.includes('john')) return 'جون دو';
  if (lower.includes('jane')) return 'جين دو';
  // simple dummy fallback
  return name.replace(/[a-z]/gi, 'ا');
}
